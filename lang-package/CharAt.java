// Java program to demonstrate
// working of charAt() method

// driver class
class CharAt 
{
	// main function
	public static void main(String args[])
	{
		String s = "Welcome! to Geeksforgeeks Planet";

		char ch = s.charAt(3);
		System.out.println(ch);

		ch = s.charAt(0);
		System.out.println(ch);
		ch = s.charAt(9);
		System.out.println(ch);
	}
}
